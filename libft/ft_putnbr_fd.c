#include "libft.h"

void	ft_putnbr_fd(int n, int fd)

{
	int	a;

	if (n == -2147483648)
		write(fd, "-2147483648", 11);
	else
	{
		if (n < 0)
		{
			n = n * (-1);
			write(fd, "-", 1);
		}
		a = n % 10;
		n = n / 10;
		if (n > 0)
			ft_putnbr_fd(n, fd);
		a += 48;
		write(fd, &a, 1);
	}
}
